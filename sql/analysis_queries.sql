-- Query 38: Percentile Analysis of Campaign Revenue
-- Purpose: Finds the 90th percentile of campaign revenue to define benchmark thresholds.
SELECT percentile_cont(0.9) WITHIN GROUP (ORDER BY revenue) AS revenue_90th_percentile
FROM marketing_campaigns;

-- Query 39: ROI Decile Classification (NTILE())
-- Purpose: Divides all campaigns into 10 groups (deciles) based on ROI.
SELECT campaign_id, 
       brand, 
       roi,
       NTILE(10) OVER (ORDER BY roi DESC) AS roi_decile
FROM marketing_campaigns;

-- Query 40: Revenue Contribution Percentage within Brand (Window Partition)
-- Purpose: Calculates what percentage of brand revenue was driven by each campaign.
SELECT campaign_id, 
       brand, 
       revenue,
       (revenue / SUM(revenue) OVER (PARTITION BY brand)) * 100.0 AS brand_revenue_contribution_pct
FROM marketing_campaigns
ORDER BY brand, brand_revenue_contribution_pct DESC;

-- Query 41: Top 20% Campaigns by Revenue (Window Function Filter)
-- Purpose: Identifies the top-tier 20% revenue-generating campaigns.
WITH RankedCampaigns AS (
    SELECT campaign_id, 
           brand, 
           revenue,
           PERCENT_RANK() OVER (ORDER BY revenue DESC) AS revenue_percent_rank
    FROM marketing_campaigns
)
SELECT campaign_id, 
       brand, 
       revenue
FROM RankedCampaigns
WHERE revenue_percent_rank <= 0.20
ORDER BY revenue DESC;

-- Query 42: Brand Market Share Analysis
-- Purpose: Computes market share percentage of each brand based on overall revenue.
SELECT brand,
       SUM(revenue) AS brand_revenue,
       (SUM(revenue) / SUM(SUM(revenue)) OVER ()) * 100.0 AS market_share_percentage
FROM marketing_campaigns
GROUP BY brand
ORDER BY market_share_percentage DESC;

-- Query 43: Channel Market Share within Each Brand
-- Purpose: Calculates how revenue is distributed across channel groups for each brand portfolio.
WITH BrandChannelRevenue AS (
    SELECT brand, 
           channel_used, 
           SUM(revenue) AS channel_revenue
    FROM marketing_campaigns
    GROUP BY brand, channel_used
)
SELECT brand, 
       channel_used, 
       channel_revenue,
       (channel_revenue / SUM(channel_revenue) OVER (PARTITION BY brand)) * 100.0 AS channel_brand_revenue_pct
FROM BrandChannelRevenue
ORDER BY brand, channel_brand_revenue_pct DESC;

-- Query 44: Month-over-Month Revenue Growth Percentage
-- Purpose: Computes month-over-month gross revenue growth rates.
WITH MonthlyRevenue AS (
    SELECT DATE_TRUNC('month', date) AS campaign_month,
           SUM(revenue) AS current_revenue
    FROM marketing_campaigns
    GROUP BY campaign_month
)
SELECT campaign_month,
       current_revenue,
       LAG(current_revenue, 1) OVER (ORDER BY campaign_month) AS prior_revenue,
       ((current_revenue - LAG(current_revenue, 1) OVER (ORDER BY campaign_month)) / 
        LAG(current_revenue, 1) OVER (ORDER BY campaign_month)) * 100.0 AS mom_growth_percentage
FROM MonthlyRevenue
ORDER BY campaign_month;

-- Query 45: Campaign Bounded Rankings within Brand Portfolios (Top 5 per Brand)
-- Purpose: Ranks campaigns by revenue within their brands and displays the top 5 per brand.
WITH RankedBrandCampaigns AS (
    SELECT campaign_id, 
           brand, 
           revenue,
           DENSE_RANK() OVER (PARTITION BY brand ORDER BY revenue DESC) AS brand_rank
    FROM marketing_campaigns
)
SELECT campaign_id, 
       brand, 
       revenue, 
       brand_rank
FROM RankedBrandCampaigns
WHERE brand_rank <= 5
ORDER BY brand, brand_rank;

-- Query 46: Highest Month-over-Month Revenue Growth Month
-- Purpose: Pinpoints the calendar month that achieved the largest positive revenue growth rate.
WITH MonthlyGrowth AS (
    SELECT DATE_TRUNC('month', date) AS campaign_month,
           SUM(revenue) AS current_revenue,
           LAG(SUM(revenue), 1) OVER (ORDER BY DATE_TRUNC('month', date)) AS prior_revenue
    FROM marketing_campaigns
    GROUP BY DATE_TRUNC('month', date)
)
SELECT campaign_month,
       current_revenue,
       prior_revenue,
       ((current_revenue - prior_revenue) / prior_revenue) * 100.0 AS growth_rate_pct
FROM MonthlyGrowth
WHERE prior_revenue IS NOT NULL
ORDER BY growth_rate_pct DESC
LIMIT 1;

-- Query 47: Rolling 30-Day Cumulative Revenue (Moving Total)
-- Purpose: Calculates a trailing 30-day cumulative revenue sum chronologically.
WITH DailyRevenue AS (
    SELECT date, 
           SUM(revenue) AS daily_revenue
    FROM marketing_campaigns
    GROUP BY date
)
SELECT date, 
       daily_revenue,
       SUM(daily_revenue) OVER (
           ORDER BY date 
           RANGE BETWEEN INTERVAL '29 days' PRECEDING AND CURRENT ROW
       ) AS rolling_30day_revenue
FROM DailyRevenue
ORDER BY date;

