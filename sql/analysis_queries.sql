-- Query 28: Brand Cumulative Running Total Revenue
-- Purpose: Computes the cumulative running total of revenue for each brand chronologically.
--          Utilizes CTEs and window partition functions.
WITH DailyBrandRevenue AS (
    SELECT brand, 
           date, 
           SUM(revenue) AS daily_revenue
    FROM marketing_campaigns
    GROUP BY brand, date
)
SELECT brand, 
       date, 
       daily_revenue,
       SUM(daily_revenue) OVER (
           PARTITION BY brand 
           ORDER BY date 
           ROWS BETWEEN UNBOUNDED PRECEDING AND CURRENT ROW
       ) AS running_total_revenue
FROM DailyBrandRevenue
ORDER BY brand, date;

-- Query 29: Brand 7-Day Moving Average of Campaign ROI
-- Purpose: Computes the 7-day trailing moving average of campaign ROI by brand.
--          Helps smooth out daily variations to track trends.
WITH DailyBrandROI AS (
    SELECT brand, 
           date, 
           AVG(roi) AS daily_avg_roi
    FROM marketing_campaigns
    GROUP BY brand, date
)
SELECT brand, 
       date, 
       daily_avg_roi,
       AVG(daily_avg_roi) OVER (
           PARTITION BY brand 
           ORDER BY date 
           ROWS BETWEEN 6 PRECEDING AND CURRENT ROW
       ) AS moving_avg_roi_7day
FROM DailyBrandROI
ORDER BY brand, date;

-- Query 30: Campaign Performance Analytical View
-- Purpose: Creates a reporting view that partitions monthly brand performance
--          and implements ROW_NUMBER(), RANK(), DENSE_RANK(), LAG(), and LEAD() 
--          to calculate month-over-month differences.
CREATE OR REPLACE VIEW view_brand_monthly_rankings AS
WITH BrandMonthlyPerformance AS (
    SELECT brand, 
           DATE_TRUNC('month', date) AS campaign_month,
           SUM(revenue) AS total_revenue,
           AVG(roi) AS avg_roi
    FROM marketing_campaigns
    GROUP BY brand, campaign_month
)
SELECT brand, 
       campaign_month, 
       total_revenue,
       
       -- Row numbering for ordering
       ROW_NUMBER() OVER (
           PARTITION BY brand 
           ORDER BY total_revenue DESC
       ) AS revenue_row_num,
       
       -- Ranking within each month
       RANK() OVER (
           PARTITION BY campaign_month 
           ORDER BY total_revenue DESC
       ) AS monthly_revenue_rank,
       
       -- Dense Ranking within each month
       DENSE_RANK() OVER (
           PARTITION BY campaign_month 
           ORDER BY total_revenue DESC
       ) AS monthly_revenue_dense_rank,
       
       -- Lagging metric: Prior month's revenue to calculate growth
       LAG(total_revenue, 1) OVER (
           PARTITION BY brand 
           ORDER BY campaign_month
       ) AS prior_month_revenue,
       
       -- Leading metric: Next month's revenue for forecast checks
       LEAD(total_revenue, 1) OVER (
           PARTITION BY brand 
           ORDER BY campaign_month
       ) AS next_month_revenue
FROM BrandMonthlyPerformance;

